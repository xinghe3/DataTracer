        #region 构造函数资源释放


        private IConnectionMultiplexer Connect()
        {
            
            try
            {
                IConnectionMultiplexer _connMultiplexer = ConnectionMultiplexer.Connect(cacheSetting.Connection);
                AddRegisterEvent(_connMultiplexer);
                return _connMultiplexer;
            }
            catch (Exception ex)
            {
                Logs.WriteExLog( ex, "RedisConnectionString:[", cacheSetting.Connection, "] Redis.DefaultKey: ", DefaultKey);
            }
            return null;
        }

        public RedisUtils(CacheSetting cacheSetting)
        {
            DefaultKey = cacheSetting.PefixKey;
            this.cacheSetting = cacheSetting;
            _connMultiplexer = GetConnectionRedisMultiplexer();
        }
        ~RedisUtils()
        {
            Dispose();
        }


        public void Dispose()
        {
            if (_IsDisposable)
            {
                Dispose(true);
            }
        }
        private void Dispose(bool value)
        {
            if (_IsDisposable)
            {
                _IsDisposable = false;
                if (_connMultiplexer != null) _connMultiplexer.Dispose();
                if (value)
                {
                    GC.SuppressFinalize(this);
                }
            }
        }
