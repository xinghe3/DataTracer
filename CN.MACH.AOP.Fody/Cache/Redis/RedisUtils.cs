        private IDatabase GetDB()
        {
            try
            {
                if (_db != null) _db.KeyExists("1");
            }
            catch
            {
                _db = null;
            }
            if (!IsConnMultiplexerConnected() || !IsDBConnected())
            {
                int n = 0;
                do
                {
                    System.Threading.Thread.Sleep(1000);
                    _connMultiplexer = Connect();
                    if (_connMultiplexer != null)
                    {
                        try
                        {
                            _connMultiplexer.GetDatabase(-1).KeyExists("1");
                        }
                        catch
                        {
                            _connMultiplexer.Dispose();
                            _connMultiplexer = null;
                        }
                    }

                    n++;
                } while (_connMultiplexer == null && n < 10);
                _db = _connMultiplexer.GetDatabase(-1);
            }
            return _db;
        }

