        /// <summary>
        /// 获取 Redis 连接对象
        /// </summary>
        /// <returns></returns>
        public IConnectionMultiplexer GetConnectionRedisMultiplexer()
        {
            if (!IsConnMultiplexerConnected() || !IsDBConnected())
            {
                lock (Locker)
                {
                    if (!IsConnMultiplexerConnected() || !IsDBConnected())
                    {
                        //if (_connMultiplexer != null) _connMultiplexer.Dispose();
                        _connMultiplexer = Connect();
                    }
                }
            }
            return _connMultiplexer;
        }

        private bool IsDBConnected()
        {
            return (_db != null) && (_db.Multiplexer != null) && _db.Multiplexer.IsConnected;
        }

        private bool IsConnMultiplexerConnected()
        {
            return (_connMultiplexer != null) && _connMultiplexer.IsConnected;
        }

        #region 其它
        private ConfigurationOptions BuildConfigurationOptions()
        {
            ConfigurationOptions conf = ConfigurationOptions.Parse(cacheSetting.Connection);

            return conf;
        }
